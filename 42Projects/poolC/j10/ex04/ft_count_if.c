int	ft_count_if(char **tab, int (*f)(char*))
{
	int j;
	int i;

	j = 0;
	i = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			j++;
		i++;
	}
	return (j);
}
