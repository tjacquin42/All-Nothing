char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'k' && str[i] <= 'z')\
				|| (str[i] >= 'K' && str[i] <= 'Z'))
			str[i] = str[i] - 10;
		else if ((str[i] >= 'a' && str[i] <= 'j')\
				|| (str[i] >= 'A' && str[i] <= 'J'))
			str[i] = str[i] + 16;
		i++;
	}
	return (str);
}
