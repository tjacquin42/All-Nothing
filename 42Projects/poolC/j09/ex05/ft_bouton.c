int	ft_bouton(int i, int j, int k)
{
	if ((i > j && j > k) || (i < j && j < k))
		return (j);
	else if ((j > k && k > i) || (j < k && k < i))
		return (k);
	else if ((j > i && i > k) || (j < i && i < k))
		return (i);
	return (0);
}
