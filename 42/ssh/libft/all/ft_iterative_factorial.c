int	ft_iterative_factorial(int nb)
{
	int v;
	int j;

	v = 1;
	j = 1;
	if ((nb > 13) || (0 > nb))
		return (0);
	while (nb >= v)
	{
		j = j * v;
		v++;
	}
	return (j);
}
