#include <string.h>
#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*dest;
	size_t	i;

	i = 0;
	if ((dest = malloc((size + 1) * sizeof(*dest))) == NULL)
		return (NULL);
	ft_bzero(dest, size + 1);
	return (dest);
}
